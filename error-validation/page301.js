// ৩. Syntax Error আর Type Error এর ডিফারেন্স কি?
// Syntax Error : যদি ব্র্যাকেট, সেমিকোলন না দেওয়া হয় অথবা বানান ভুল হয়, তাহলে Syntax Error হয়।

// Type Error : একটি সংখ্যা নিয়ে স্ট্রিংয়ের কোনো ফাংশন ব্যবহার করার চেষ্টা করলে Type Error হয়।