// ২. তোর ক্লাসে 45 জন ছাত্র আছে, আর পাশের ক্লাসে 50 জন। একটা প্রোগ্রাম লিখে দেখ, কোন ক্লাসে বেশি ছাত্র আছে।
const myClass = 45
const otherClass = 50
console.log(myClass < otherClass)




// ৩. তুই আর তোর বন্ধু পরীক্ষায় নাম্বার পেয়েছিস। তুই 75 পেয়েছিস, আর তোর বন্ধু পেয়েছে 75। একটা প্রোগ্রাম লিখে চেক কর, তোর আর তোর বন্ধুর নাম্বার সমান কি না।
const myNumber = 75
const myFriendNumber = 75
console.log(myNumber == myFriendNumber)




// ৪. তুই 100 টাকা জমানোর টার্গেট করেছিস। এখন পর্যন্ত 95 টাকা জমাইছিস। একটা প্রোগ্রাম লিখে চেক কর, তুই টার্গেটে পৌঁছাইছিস কি না বা তার চেয়ে বেশি জমাইছিস কি না।
const targetedTaka = 100
const actualTaka = 95
console.log(targetedTaka <= actualTaka)



// ৫. একটা প্রোগ্রাম লিখে দেখ, 10 এবং 7 কি অ-সমান (Different)।
